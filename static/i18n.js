    function getLang() {
        return localStorage.getItem('lang') || 'ru';
    }

    function setLang(lang) {
        localStorage.setItem('lang', lang);
        applyLang(lang);
    }

    function applyLang(lang) {
        const map = translations[lang] || translations.ru;
        // Устанавливаем атрибут html.lang для правильного чтения
        try {
            document.documentElement.lang = (lang === 'kz') ? 'kk' : 'ru';
            document.documentElement.setAttribute('data-lang', lang);
        } catch (e) { }

        // Заменяем элементы с data-i18n
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const text = map[key];
            if (!text) return;
            if (el.placeholder !== undefined && (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA')) {
                el.placeholder = text;
            } else if (el.tagName === 'OPTION') {
                el.textContent = text;
            } else {
                el.innerHTML = text;
            }
        });

        // Специально обрабатываем placeholder для поиска
        const searchInput = document.getElementById('search');
        if (searchInput) {
            const placeholder = map['search.placeholder'];
            if (placeholder) searchInput.placeholder = placeholder;
        }
    }

    // Навешивание обработчиков на языковую панель
    document.addEventListener('DOMContentLoaded', function () {
        const lang = getLang();
        applyLang(lang);

        const langToggle = document.getElementById('lang-toggle');
        const langPanel = document.getElementById('lang-panel');
        if (langToggle && langPanel) {
            langToggle.textContent = lang.toUpperCase();
            langToggle.addEventListener('click', () => {
                langPanel.classList.toggle('open');
            });
            langPanel.querySelectorAll('.lang-option').forEach(btn => {
                btn.addEventListener('click', (e) => {
                    const l = btn.getAttribute('data-lang');
                    setLang(l);
                    langPanel.classList.remove('open');
                    if (langToggle) langToggle.textContent = l.toUpperCase();
                });
            });
        }

        // Закрыть панель при клике вне
        document.addEventListener('click', (e) => {
            const panel = document.getElementById('lang-panel');
            const toggle = document.getElementById('lang-toggle');
            if (!panel || !toggle) return;
            if (!panel.contains(e.target) && !toggle.contains(e.target)) {
                panel.classList.remove('open');
            }
        });
    });

